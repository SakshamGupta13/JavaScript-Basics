// boolean and comparision operator

// booleans -> true, false

let num1="5";
let num2=5;
// console.log(num1>=num2);

// == vs ===
console.log(num1 == num2);  // only takes values irrespective of datatypes
console.log(num1 === num2);  // takes both values and datatypes


// != vs !==
console.log(num1 != num2);  // means both value is true if it gives false
console.log(num1 !=- num2);  // means both value is false if it gives true

